package junit;

/**
 * Ein einfacher Taschenrechner.
 */
public class Taschenrechner {
	
	/**
	 * Addiert zwei ganzzahlige Zahlen und liefert das Ergebnis zurueck.
	 * @param a Erste Zahl
	 * @param b Zweite Zahl
	 * @return Addition beider Zahlen als Integer
	 */
	public int addiere(int a, int b) {
		
		return a + b;
	}
	
	/**
	 * Multipliziert zwei ganzzahlige Zahlen und liefert das Ergebnis zurueck.
	 * @param a Erste Zahl
	 * @param b Zweite Zahl
	 * @return Multiplikation beider Zahlen als Integer
	 */
	public int multipliziere(int a, int b) {
		
		return a * b;
	}

}
